package cls_18;

public class Wapper_class {
	public static void main(String[] args) {

		Integer a1 = 89;
		int a = 89;
		System.out.println(a1);
		System.out.println(a);

		// auto-boxing
		Integer b = 12;
		int b1 = 17;
		b = b1;
		System.out.println(b);
		// un-Boxing

		Integer c = 78;
		int c1 = 89;
		c1 = c;
		System.out.println(c1);

	}

}
